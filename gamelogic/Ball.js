
class Ball {
    constructor(radius, x, y, vx, vy, canvasWidth, canvasHeight) {
        this.radius = radius;
        this.x = x;
        this.y = y;
        this.vx = vx;
        this.vy = vy;
        this.canvasWidth = canvasWidth;
        this.canvasHeight = canvasHeight;
    }
    
}
module.exports = Ball;